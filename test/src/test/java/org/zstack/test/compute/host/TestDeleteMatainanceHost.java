public class TestDeleteMatainanceHost {
    Deployer deployer;
    Api api;
    ComponentLoader loader;
    CloudBus bus;
    DatabaseFacade dbf;
    SessionInventory session;
    SMPPrimaryStorageSimulatorConfig config;
    KVMSimulatorConfig kconfig;

    @Before
    public void setUp() throws Exception {
        DBUtil.reDeployDB();
        WebBeanConstructor con = new WebBeanConstructor();
        deployer = new Deployer("deployerXml/host/TestDeleteMatainanceHost.xml", con);
        deployer.addSpringConfig("KVMRelated.xml");
        deployer.build();
        api = deployer.getApi();
        loader = deployer.getComponentLoader();
        bus = loader.getComponent(CloudBus.class);
        dbf = loader.getComponent(DatabaseFacade.class);
        kconfig = loader.getComponent(KVMSimulatorConfig.class);
        config = loader.getComponent(SMPPrimaryStorageSimulatorConfig.class);
        session = api.loginAsAdmin();
    }

    @Test
    public void test() throws ApiSenderException {
        HostInventory inv = deployer.hosts.get("TestHost");

        HostVO vo = dbf.findByUuid(inv.getUuid(), HostVO.class);
        Assert.assertNotNull(vo);

        vo.setState(HostState.Maintenance);
        dbf.updateAndRefresh(vo);

        api.deleteHost(inv.getUuid());
        vo = dbf.findByUuid(inv.getUuid(), HostVO.class);
        Assert.assertNull(vo);
    }
}
